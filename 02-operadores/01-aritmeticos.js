//Declaración
let a = 5;
let b = 7;

//Expresión
console.log(a + b, "adición");
console.log(a - b, "resta");
console.log(a * b, "multiplicación");
console.log(a / b, "división");
console.log(a % b, "módulo");
console.log(a ** b, "exponenciación");

//incrementar
console.log(++a);
console.log(a++);
console.log(a);
//decrementar
console.log(--a);
console.log(a--);
console.log(a);
